DESC CLIENTE;

SELECT 
    TO_CHAR(NUMRUT_CLI, '999G999G999')|| '-' || DVRUT_CLI AS "Rut cliente",
    INITCAP(NOMBRE_CLI || ' ' || APPATERNO_CLI || ' ' || APMATERNO_CLI) AS "Nombre cliente",
